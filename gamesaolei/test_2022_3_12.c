#include"game.h"

void menu()
{
	printf("****************************************\n");
	printf("*****     1 .play        0. exit    ****\n");
	printf("****************************************\n");
}


void game() 
{
	//雷的信息
	//1.布置好累的信息
	char mine[ROWS][COLS] = { 0 };
	//2.排查雷的信息
	char show[ROWS][COLS] = { 0 };
	//初始化
	InitBoard(show, ROWS, COLS,'*');
	InitBoard(mine, ROWS, COLS,'0');
	//打印棋盘
	DisplayBoard(mine, ROW, COL);
	DisplayBoard(show, ROW, COL);
	//布置雷
	SetMine(mine, ROW, COL);
	//扫雷
	FindMine(mine,show, ROW, COL);



}
	


void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
	menu();
	printf("请选择：");
	scanf_s("%d", &input);
	switch (input)
	{
	case 0:
		printf("退出游戏\n");
		break;
	case 1:
		printf("进入游戏\n");
		game();
		break;
	default:
		printf("选择错误，重新选择\n");
		break;
	}
	} while (input);

}





int main() {
	test();
	return 0;
}